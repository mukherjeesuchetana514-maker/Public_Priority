function changeLanguage(lang) {
  if (!translations[lang]) return;

  const elements = document.querySelectorAll("[data-i18n]");
  
  elements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      // If it's an input or textarea, we update placeholder, otherwise textContent
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else if (el.tagName === 'OPTION') {
        el.textContent = translations[lang][key];
      } else {
        // We preserve any child elements like icons by only updating the text node,
        // or we just replace innerHTML. Let's replace innerHTML if there's no icons, 
        // but for buttons with icons, we should wrap the text in a span.
        // For simplicity, let's just replace innerHTML if there's icons, but our dictionary doesn't have icons.
        // Wait, if it has an icon, it will be wiped out if we just set textContent.
        // Let's implement a safe text replacement that only changes the text node.
        let hasIcon = el.querySelector('i');
        if (hasIcon) {
           // Find the text node and replace it
           for (let i = 0; i < el.childNodes.length; i++) {
             if (el.childNodes[i].nodeType === 3 && el.childNodes[i].nodeValue.trim() !== '') {
               el.childNodes[i].nodeValue = " " + translations[lang][key] + " ";
               break;
             }
           }
        } else {
           el.textContent = translations[lang][key];
        }
      }
    }
  });

  document.documentElement.lang = lang;
}

// Initial load check
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem('appLanguage') || 'en';
    if (savedLang !== 'en') {
        changeLanguage(savedLang);
    }
});
